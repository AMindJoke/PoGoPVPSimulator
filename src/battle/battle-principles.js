const PRINCIPLE_BY_ID = new Map(BATTLE_PRINCIPLES.map(item => [item.id, item]));

function validateBattlePrincipleRegistry(principles = BATTLE_PRINCIPLES) {
  const requiredFields = [
    "id",
    "category",
    "name",
    "description",
    "ownerLayer",
    "priority",
    "condition",
    "inputs",
    "output",
    "forbiddenSideEffects",
    "reasonCodes",
    "source",
    "status",
    "introducedInVersion",
    "modifiedInVersion",
    "tests"
  ];
  const errors = [];
  const ids = new Set();
  for (const item of principles) {
    for (const field of requiredFields) {
      if (item[field] === undefined || item[field] === null || item[field] === "") errors.push(`${item.id || "unknown"} missing ${field}`);
    }
    if (ids.has(item.id)) errors.push(`Duplicate principle id ${item.id}`);
    ids.add(item.id);
    if (!Object.values(PRINCIPLE_STATUS).includes(item.status)) errors.push(`${item.id} has invalid status ${item.status}`);
    if (!Array.isArray(item.inputs) || !item.inputs.length) errors.push(`${item.id} must declare inputs`);
    if (!Array.isArray(item.allowedOutputs) || !item.allowedOutputs.length) errors.push(`${item.id} must declare allowedOutputs`);
    if (!Array.isArray(item.forbiddenSideEffects) || !item.forbiddenSideEffects.length) errors.push(`${item.id} must declare forbiddenSideEffects`);
    if (!Array.isArray(item.tests) || !item.tests.length) errors.push(`${item.id} must declare tests`);
  }
  if (principles.length !== 43) errors.push(`Expected 43 principles, found ${principles.length}`);
  return {
    valid: errors.length === 0,
    errors,
    count: principles.length,
    version: PRINCIPLE_REGISTRY_VERSION
  };
}

const api = Object.freeze({
  PRINCIPLE_REGISTRY_VERSION,
  PRINCIPLE_STATUS,
  PRINCIPLE_PRIORITY_GROUPS,
  COMMON_FORBIDDEN_SIDE_EFFECTS,
  BATTLE_PRINCIPLES,
  PRINCIPLE_BY_ID,
  validateBattlePrincipleRegistry
});

